    	private class thehandler implements ActionListener{
		public void actionPerformed(ActionEvent event){

			String string = "";

			if(event.getSource()==msgBox)
			{
				string=String.format("%s", event.getActionCommand());
				String text= msgBox.getText();
				clientAction.ClientOutServerIn(text);
				msgBox.setText("");
			}
			else if(event.getSource()==nickName) {
				string=String.format("%s", event.getActionCommand());
				if(string.matches("[0-9]*"))
				{
					JOptionPane.showMessageDialog(null,"desteklenmeyen bi√ßim");
					nickName.setText("");
				}
				else
				{
					clientAction.setName(string);
					clientAction.SetClient("1",string);
					JOptionPane.showMessageDialog(null, "nickname: "+string+" olarak se√ßildi. ");
					nickName.setText("");
					nickName.setEditable(false);
					msgBox.setEditable(true);
					clientAction.ClientOutServerIn("userJoin");
				}
			}
			else if(event.getSource()==roomNo) {
				string=String.format("%s", event.getActionCommand());
				if(string.matches("[0-9]*"))
				{
					
                                        clientAction.c.SetRoom(string);
					JOptionPane.showMessageDialog(null, "Chat Odasƒ± deƒüi≈üti "+string);
					roomNo.setText("");
					clientAction.ClientOutServerIn("pickRoom");
				}
			}
		}
	}

